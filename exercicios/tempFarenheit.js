// Exercício 7: Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Farenheit.

let tempCelsius = prompt('Informe a temperatura em celsius: ');
const tempFarehiet = tempCelsius * 1.8 + 32;
alert(tempCelsius + ' graus Celsius corresponde a ' + tempFarehiet + ' graus Farenheit.');