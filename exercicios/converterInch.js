// Exercício 3: Faça um Programa que converta  centímetros  para polegadas. (Dica: 1 centímetro = 0,393700787 polegadas)
let converterMedida = prompt('Informe o valor em centimátros que deseja converter: ');
const InchMedida = converterMedida * 0.393700787;
alert(converterMedida + ' centímetros (cm) corresponde a ' + InchMedida + ' polegadas.')