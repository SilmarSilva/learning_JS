// Execício 2: Faça um Programa que converta metros para centímetros. (Dica:  1 metro = 100 centímetros)

let converterMedida = prompt('Informe o valor em metros que deseja converter para centímetros: ');
const resultConversion = converterMedida * 100;
alert('O valor informado foi ' + converterMedida + ' metros (m) e corresponde a ' + resultConversion + ' (cm) centímetros.');